/**
 * Rendert ein Icon basierend auf dem Namen
 */
export function NavIconComponent({
  name,
  className = 'h-5 w-5',
}: {
  name: NavIcon
  className?: string
}) {
  const Icon = ICON_MAP[name]
  return Icon ? <Icon className={className} /> : null
}

